"""
Output Module — Export STIX JSON, Markdown, Metrics
=====================================================
Formats and exports analysis results.
"""
from __future__ import annotations
import json, uuid
from datetime import datetime, timezone
from typing import Any, Optional
from pathlib import Path
import structlog

logger = structlog.get_logger(__name__)

def create_stix_bundle(objects: list[dict[str, Any]]) -> dict[str, Any]:
    """Wrap STIX objects in a STIX 2.1 bundle."""
    return {
        "type": "bundle",
        "id": f"bundle--{uuid.uuid4()}",
        "objects": objects,
    }

def export_stix_json(objects: list[dict[str, Any]], filepath: Path | None = None) -> str:
    """Export STIX bundle as JSON string, optionally saving to file."""
    bundle = create_stix_bundle(objects)
    json_str = json.dumps(bundle, indent=2, default=str)
    if filepath:
        filepath.parent.mkdir(parents=True, exist_ok=True)
        filepath.write_text(json_str, encoding="utf-8")
        logger.info("STIX bundle exported", path=str(filepath))
    return json_str

def export_markdown_report(analysis: dict[str, Any], guardrail_results: dict[str, Any] | None = None) -> str:
    """Generate a Markdown report from analysis results."""
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    lines = [
        "# 🛡️ CTI-Shield Threat Analysis Report",
        f"\n**Generated:** {now}",
        f"\n**Mode:** {analysis.get('mode', 'demo')}",
        f"\n**Latency:** {analysis.get('latency_ms', 'N/A')}ms",
        "\n---\n",
    ]
    
    # Analysis summary
    a = analysis.get("analysis", {})
    if isinstance(a, dict):
        summary = a.get("summary", str(a))
    else:
        summary = str(a)
    lines.append("## 📋 Analysis Summary\n")
    lines.append(summary)
    
    # TTPs
    ttps = a.get("ttps", []) if isinstance(a, dict) else []
    if ttps:
        lines.append("\n## 🎯 MITRE ATT&CK Techniques\n")
        lines.append("| ID | Name | Tactic |")
        lines.append("|---|---|---|")
        for ttp in ttps:
            lines.append(f"| {ttp.get('id', '')} | {ttp.get('name', '')} | {ttp.get('tactic', '')} |")
    
    # STIX Objects
    stix = analysis.get("stix_objects", [])
    if stix:
        lines.append(f"\n## 📦 STIX 2.1 Objects ({len(stix)} generated)\n")
        for obj in stix:
            lines.append(f"- **{obj.get('type', 'unknown')}**: {obj.get('name', obj.get('id', ''))}")
    
    # Guardrail results
    if guardrail_results:
        lines.append("\n## ✅ Guardrail Validation\n")
        lines.append(f"- **Passed:** {'Yes ✅' if guardrail_results.get('passed') else 'No ❌'}")
        lines.append(f"- **Attempts:** {guardrail_results.get('attempt', 'N/A')}")
        
        hall = guardrail_results.get("hallucination", {})
        if hall:
            lines.append(f"- **Hallucination Rate:** {hall.get('hallucination_rate', 'N/A')}")
            lines.append(f"- **Grounded Claims:** {hall.get('grounded', 0)}/{hall.get('total', 0)}")
        
        stix_val = guardrail_results.get("stix_validation", {})
        if stix_val:
            lines.append(f"- **STIX Compliance:** {stix_val.get('overall_compliance', 'N/A'):.1%}")
        
        if guardrail_results.get("escalated"):
            lines.append(f"\n> ⚠️ **Escalated for human review:** {guardrail_results.get('escalation_reason', '')}")
    
    lines.append("\n---\n*Report generated by CTI-Shield*")
    return "\n".join(lines)

def compute_metrics(analysis: dict[str, Any], guardrail_results: dict[str, Any] | None = None) -> dict[str, Any]:
    """Compute evaluation metrics for display."""
    metrics = {
        "response_latency_ms": analysis.get("latency_ms", 0),
        "mode": analysis.get("mode", "demo"),
        "timestamp": datetime.now(timezone.utc).isoformat(),
    }
    
    if guardrail_results:
        hall = guardrail_results.get("hallucination", {})
        metrics["hallucination_rate"] = hall.get("hallucination_rate", 0)
        metrics["grounded_claims"] = hall.get("grounded", 0)
        metrics["total_claims"] = hall.get("total", 0)
        metrics["factual_consistency"] = 1 - hall.get("hallucination_rate", 0)
        
        stix_val = guardrail_results.get("stix_validation", {})
        metrics["stix_compliance"] = stix_val.get("overall_compliance", 0)
        metrics["stix_valid_count"] = stix_val.get("valid", 0)
        metrics["stix_invalid_count"] = stix_val.get("invalid", 0)
    
    a = analysis.get("analysis", {})
    if isinstance(a, dict):
        ttps = a.get("ttps", [])
        metrics["ttp_count"] = len(ttps)
    
    return metrics

def format_metrics_table(metrics: dict[str, Any]) -> str:
    """Format metrics as a readable table string."""
    lines = ["| Metric | Value |", "|---|---|"]
    display = {
        "Hallucination Rate": f"{metrics.get('hallucination_rate', 0):.1%}",
        "STIX Compliance": f"{metrics.get('stix_compliance', 0):.1%}",
        "Factual Consistency": f"{metrics.get('factual_consistency', 0):.1%}",
        "Response Latency": f"{metrics.get('response_latency_ms', 0):.0f}ms",
        "TTPs Identified": str(metrics.get("ttp_count", 0)),
        "Mode": metrics.get("mode", "demo"),
    }
    for k, v in display.items():
        lines.append(f"| {k} | {v} |")
    return "\n".join(lines)
